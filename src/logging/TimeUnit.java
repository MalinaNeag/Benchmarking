package logging;

/**
 * The TimeUnit enum represents different units of time.
 */
public enum TimeUnit {
    /**
    * Represents the unit of time of a nanosecond.
    */
    Nano,
    /**
     * Represents the unit of time of a microsecond.
     */
    Micro,
    /**
     * Represents the unit of time of a millisecond.
     */
    Milli,
    /**
     * Represents the unit of time of a second.
     */
    Sec
}
